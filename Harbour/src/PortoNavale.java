
public class PortoNavale {

	private String name;
	private int numNavi;
	private int mq;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumNavi() {
		return numNavi;
	}
	public void setNumNavi(int numNavi) {
		this.numNavi = numNavi;
	}
	public int getMq() {
		return mq;
	}
	public void setMq(int mq) {
		this.mq = mq;
	}
	
	
}
